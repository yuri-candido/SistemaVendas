    public void Listar() throws Exception{
        
       FornecedorDAO dao = new FornecedorDAO();
       List<Fornecedores> lista = dao.listarFornecedor();
       DefaultTableModel dados = (DefaultTableModel) tabelaFornecedores.getModel();
       dados.setNumRows(0);
       
       for(Fornecedores c: lista){
           dados.addRow(new Object[]{
               
               c.getId(),
               c.getNome(),               
               c.getCnpj(),
               c.getEmail(),
               c.getTelefone(),
               c.getCelular(),
               c.getCep(),
               c.getEndereco(),
               c.getNumero(),
               c.getComplemento(),
               c.getBairro(),
               c.getCidade(),
               c.getUf()                                            
           });
       }
    }   
    
    
