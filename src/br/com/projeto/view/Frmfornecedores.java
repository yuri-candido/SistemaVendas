    private void btnpesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnpesquisarActionPerformed
        
       String nome = "%"+txtpesquisa.getText()+"%";
        
        
       FornecedorDAO dao = null;
        try {
            dao = new FornecedorDAO();
        } catch (Exception ex) {
            Logger.getLogger(Frmfornecedores.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
       List<Fornecedores> lista = dao.buscarFornecedor(nome);
       DefaultTableModel dados = (DefaultTableModel) tabelaFornecedores.getModel();
       dados.setNumRows(0);
       
       for(Fornecedores c: lista){
           dados.addRow(new Object[]{
               
               c.getId(),
               c.getNome(),
               
               c.getCnpj(),
               c.getEmail(),
               c.getTelefone(),
               c.getCelular(),
               c.getCep(),
               c.getEndereco(),
               c.getNumero(),
               c.getComplemento(),
               c.getBairro(),
               c.getCidade(),
               c.getUf()                                            
           });
       }
        
        
        
    }//GEN-LAST:event_btnpesquisarActionPerformed

